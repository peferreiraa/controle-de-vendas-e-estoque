    private void btnConsultarTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarTotalActionPerformed
        try{
            DateTimeFormatter format = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataVenda = LocalDate.parse(txtData.getText(), format);
            
            double totalVenda;
            
            SaleDao dao = new SaleDao();
            totalVenda = dao.returnTotalSaleByDate(dataVenda);
            
            txtTotalVendas.setText(String.valueOf(totalVenda));
        }
        catch(Exception error){
            JOptionPane.showMessageDialog(null, "Data inv√°ldia " + error);
        }
    }//GEN-LAST:event_btnConsultarTotalActionPerformed

