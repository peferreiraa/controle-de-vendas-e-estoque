    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
       
        
        
        DateTimeFormatter format = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataInicio = LocalDate.parse(txtDataInicial.getText(), format);
        LocalDate dataFinal = LocalDate.parse(txtDataFinal.getText(), format);
        
        SaleDao dao = new SaleDao();
        
        List<Sale> list = dao.toListProductByDate(dataInicio, dataFinal);
        
        DefaultTableModel dados = (DefaultTableModel)historicTable.getModel();
        dados.setNumRows(0);
        
        for(Sale s: list){
            dados.addRow(new Object[]{
                s.getId(),
                s.getDataVenda(),
                s.getClient().getNome(),
                s.getTotalVendas(),
                s.getObservacao()
            
            });
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void historicTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_historicTableMouseClicked
        FrmDetailsSale viewDetails = new FrmDetailsSale();
        
        viewDetails.txtCliente.setText(historicTable.getValueAt(historicTable.getSelectedRow(), 2).toString());
        viewDetails.txtTotalVenda.setText(historicTable.getValueAt(historicTable.getSelectedRow(), 3).toString());
        viewDetails.txtDataVenda.setText(historicTable.getValueAt(historicTable.getSelectedRow(), 1).toString());
        viewDetails.txtObsVenda.setText(historicTable.getValueAt(historicTable.getSelectedRow(), 4).toString());
        
        
        
        //Items comprados
        int vendaId = Integer.parseInt(historicTable.getValueAt(historicTable.getSelectedRow(), 0).toString());
        
        SalesItems item = new SalesItems();
        SalesItemsDao daoItem = new SalesItemsDao();
        List<SalesItems> itemsList = daoItem.toListSalesItems(vendaId);
        
        DefaultTableModel dados = (DefaultTableModel)viewDetails.detailsSaleTable.getModel();
        dados.setNumRows(0);
        
        for(SalesItems i : itemsList){
            dados.addRow(new Object[]{
                i.getProduto().getDescricao(),
                i.getQtd(),
                i.getProduto().getPreco(),
                i.getSubtotal()
            });

        }
        
        viewDetails.setVisible(true);
    }//GEN-LAST:event_historicTableMouseClicked

